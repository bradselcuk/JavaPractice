package W3_Subjects;

public class L2_WhileLoop {
    public static void main(String[] args) {

        int i = 0;
        while(i<=5){
            System.out.println(i);
            i++;
        }
        int j =5;
        while (j>0){
            System.out.println(j);
            j--;
        }
    }
}
